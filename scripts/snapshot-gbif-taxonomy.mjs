#!/usr/bin/env node

import { writeFile, mkdir } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { execFile } from 'node:child_process';
import { promisify } from 'node:util';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const OUTPUT_PATH = path.resolve(repoRoot, 'packages/core/src/data/gbif-taxonomy-cache.ts');
const GBIF_SPECIES_SEARCH_URL = 'https://api.gbif.org/v1/species/search';
const PLANTAE_KEY = 6;
const PAGE_SIZE = 1000;
const RANKS = ['PHYLUM', 'CLASS', 'ORDER', 'FAMILY', 'GENUS'];
const execFileAsync = promisify(execFile);

const rankPriority = {
  PHYLUM: 1,
  CLASS: 2,
  ORDER: 3,
  FAMILY: 4,
  GENUS: 5,
};

function normalizeName(value) {
  return value
    .trim()
    .normalize('NFD')
    .replace(/\p{Diacritic}/gu, '')
    .replace(/\s+/g, ' ')
    .toLowerCase();
}

async function fetchRankEntries(rank) {
  let offset = 0;
  let endOfRecords = false;
  const entries = [];

  while (!endOfRecords) {
    const query = new URLSearchParams({
      highertaxon_key: String(PLANTAE_KEY),
      rank,
      status: 'ACCEPTED',
      limit: String(PAGE_SIZE),
      offset: String(offset),
    });

    const { stdout } = await execFileAsync(
      'curl',
      [
        '-sS',
        '-H',
        'Accept: application/json',
        `${GBIF_SPECIES_SEARCH_URL}?${query.toString()}`,
      ],
      { maxBuffer: 25 * 1024 * 1024 },
    );
    const payload = JSON.parse(stdout);
    const results = Array.isArray(payload.results) ? payload.results : [];

    for (const item of results) {
      const canonicalName = item.canonicalName || item.scientificName;
      if (!canonicalName) continue;

      entries.push({
        key: item.nubKey || item.key,
        canonicalName,
        rank,
        normalized: normalizeName(canonicalName),
      });
    }

    endOfRecords = Boolean(payload.endOfRecords);
    offset += PAGE_SIZE;
  }

  return entries;
}

function toTypeScript(cache) {
  const rankUnion = RANKS.map((rank) => `'${rank}'`).join(' | ');
  const { generatedAt, highertaxonKey, totalEntries, countsByRank, entries } = cache;
  const escapedEntriesJson = JSON.stringify(entries).replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  return `// AUTO-GENERATED FILE. DO NOT EDIT MANUALLY.\n// Generated by scripts/snapshot-gbif-taxonomy.mjs\n\nexport type CachedTaxonomyRank = ${rankUnion};\n\nexport interface CachedTaxonomyEntry {\n  key: number;\n  canonicalName: string;\n  rank: CachedTaxonomyRank;\n  normalized: string;\n}\n\nexport interface GBIFTaxonomyCache {\n  generatedAt: string;\n  highertaxonKey: number;\n  totalEntries: number;\n  countsByRank: Record<CachedTaxonomyRank, number>;\n  entries: CachedTaxonomyEntry[];\n}\n\nconst CACHED_ENTRIES_JSON = '${escapedEntriesJson}';\nconst CACHED_ENTRIES = JSON.parse(CACHED_ENTRIES_JSON) as CachedTaxonomyEntry[];\n\nexport const GBIF_TAXONOMY_CACHE: GBIFTaxonomyCache = {\n  generatedAt: '${generatedAt}',\n  highertaxonKey: ${highertaxonKey},\n  totalEntries: ${totalEntries},\n  countsByRank: ${JSON.stringify(countsByRank, null, 2)} as Record<CachedTaxonomyRank, number>,\n  entries: CACHED_ENTRIES,\n};\n`;
}

async function main() {
  const allEntries = [];
  const countsByRank = Object.fromEntries(RANKS.map((rank) => [rank, 0]));

  for (const rank of RANKS) {
    const rankEntries = await fetchRankEntries(rank);
    countsByRank[rank] = rankEntries.length;
    allEntries.push(...rankEntries);
  }

  const dedupMap = new Map();
  for (const entry of allEntries) {
    const key = `${entry.rank}:${entry.key}`;
    dedupMap.set(key, entry);
  }

  const entries = Array.from(dedupMap.values()).sort((a, b) => {
    const rankDiff = rankPriority[a.rank] - rankPriority[b.rank];
    if (rankDiff !== 0) return rankDiff;
    return a.canonicalName.localeCompare(b.canonicalName);
  });

  const cache = {
    generatedAt: new Date().toISOString(),
    highertaxonKey: PLANTAE_KEY,
    totalEntries: entries.length,
    countsByRank,
    entries,
  };

  await mkdir(path.dirname(OUTPUT_PATH), { recursive: true });
  await writeFile(OUTPUT_PATH, toTypeScript(cache), 'utf8');

  console.log(`Snapshot written: ${OUTPUT_PATH}`);
  console.log(`Entries: ${entries.length}`);
  for (const rank of RANKS) {
    console.log(`${rank}: ${countsByRank[rank]}`);
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
